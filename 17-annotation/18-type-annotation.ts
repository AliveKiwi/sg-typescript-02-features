// Type Annotation in TypeScript
// TypeScript allows you to explicitly define the type of a variable using type annotations
// This can help with code clarity and catching errors early
// Type annotations are especially useful when the type cannot be inferred from the value

let apples: number = 5;
let speed: string = 'fast';
let hasName: boolean = true;
let nothingMuch: null = null;
let nothing: undefined = undefined;

// built in objects
let now: Date = new Date();
